package exemplo;

public class Exemplo02 {
    public static void main(String[] args) {
        int a, i;

        a = 5;

        System.out.println(a++);
        System.out.println(a);

        System.out.println(++a);
        System.out.println(a);

        for(i = 1; i < 10; i++){
            System.out.println(i);
        }

        System.out.println("final" + i);
    }
}
