package exemplos;

public class Exercicio07 {
    public static void main(String[] args) {
        int numero = 10;

        numero++;

        System.out.println(numero);

        System.out.println(numero + 1);

        System.out.println(numero);

        numero--;
        System.out.println(numero);

        numero += 5;

        numero = 10;

        System.out.println(numero);
    }
}
